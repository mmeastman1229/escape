def add(first_number, second_number):
    print(f'{first_number} + {second_number} = {first_number + second_number}')


def subtract(first_number, second_number):
    print(f'{first_number} - {second_number} = {first_number - second_number}')


add(5,7)
add(2012, 137)
add(1234, 4321)

subtract(7,5)
subtract(2012, 173)
subtract(1,55)
